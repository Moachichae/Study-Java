package edu.java.method02;

public class MethodMain02 {

	// 메소드 이름 : add
	// 기능 : 두 개의 정수를 매개변수로 전달받아서, 두 정수의 합을 리턴
	// 리턴타입 : int
	// 매개변수 : int x, int y
	public static int add(int x, int y) {
//		int sum = x + y;
		return x + y;
	} // end add()

	// 메소드 이름 : sub
	// 기능 : 두 개의 정수를 매개변수로 전달받아서, 두 정수의 차를 리턴
	// 리턴타입 : int
	// 매개변수 : int x, int y
	public static int sub(int x, int y) {
		return x - y;
	} // end sub()

	// 메소드 이름 : mul
	// 기능 : 두 개의 실수(double)를 전달받아서, 두 수의 곱을 리턴
	// 리턴타입 : double
	// 매개변수 : double x, double y
	public static double mul(double x, double y) {
		return x * y;
	} // end mul()

	// 메소드 이름 : div
	// 기능 : 두 개의 실수(double)를 전닯다아서, 나눈 값을 리턴
	// 리턴타입 : double
	// 매개변수 : double x, double y
	public static double div(double x, double y) {
		return x / y;
	} // end div()

	public static void main(String[] args) {
		System.out.println("메소드 연습");
		int result = add(10, 20);
		System.out.println("result = " + result);
		result = sub(100, 10);
		System.out.println("result = " + result);

		double m = mul(1.5, 2.0);
		System.out.println("m = " + m);
		
		double d = div(3, 2);
		System.out.println("d = " + d);

	} // end main()

} // end MethodMain02









